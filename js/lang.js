$(document).ready(function() {
	
	//default laguange
	changeLanguage("my");
	changePlaceHolder("my");
	changeButton("my");

	function changeLanguage(lang){
		$(".tr").each(function(i){
			$(this).text(aLangKeys[lang][$(this).attr("key")]);
		});
	}

	function changePlaceHolder(lang){
		$(".form-control").each(function(i){
			$(this).attr("placeholder",aPlaceKeys[lang][$(this).attr("name")]);
		});
	}

	function changeButton(lang){
		$(".button").each(function(i){
			$(this).attr("value",aButtonKeys[lang][$(this).attr("key")]);
		});
	}

	// onclick behavior
	$(".lang").click(function(){
		var lang = $(this).attr("lang"); // obtain language id
		var toggle = $(this).attr("toggle"); // obtain toggle language id
		// translate all translatable elements
		changeLanguage(lang);
		changePlaceHolder(lang);
		changeButton(lang);

		$(this).attr({"lang":toggle,"toggle":lang});
	});
});